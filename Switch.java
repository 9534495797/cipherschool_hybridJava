public class Switch {
    public static void main(String[] args) {
        char ch='a';
        switch (ch) {
            case 'a':
                System.out.println("hii i am a");
                break;
        case 'b':
        System.out.println("hii i am b");
        break;
            default:
             System.out.println("neither a nor b");
                break;
        }
    }
}
